class Account {
  constructor(
    email,
    username,
    password,
    name,
    phone,
    dateOfBirth,
    createdDate,
    role,
    isVerify
  ) {
    this.email = email;
    this.username = username;
    this.password = password;
    this.name = name;
    this.phone = phone;
    this.dateOfBirth = dateOfBirth;
    this.createdDate = createdDate;
    this.role = role;
    this.isVerify = isVerify;
  }
}
